// Operadores: Atribuição
console.log("Operadores de Atribuição:");

const a = 7;
let b = 3;

// Soma:
b += a; // b = b + a
console.log(b);   // Saída: 10

// Subtração:
b -= 4; // b = b - 4
console.log(b);   // Saída: 6

// Multiplicação:
b *= 2; // b = b * 2
console.log(b);   // Saída: 12

// Divisão:
b /= 2; // b = b / 2
console.log(b);   // Saída: 6

// Módulo:
b %= 2; // b = b % 2
console.log(b);   // Saída: 0