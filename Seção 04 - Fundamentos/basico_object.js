// OBJECT
console.log("OBJECT básico:")

const product1 = {};    // Representação de um objeto: {}
product1.nome = "Celular Ultra Mega";   // Chave: nome & Valor: "Celular Ultra Mega"
product1.preco = 4899.90;

console.log(product1);  // { nome: 'Celular Ultra Mega', preco: 4899.9 }

const product2 = {  // Declaração literal de um objeto, com chaves e valores
    nome: "Camisa Polo",
    preco: 199.90
}

console.log(product2);  // { nome: 'Camisa Polo', preco: 199.9 }